var Material = function(id, name, isBlock, variants) {
	this.id = id;
	this.name = "minecraft:" + name;
	this.isBlock = isBlock;
	if (variants instanceof Array) {
		this.variants = variants;
		this.maxDurability = 0;
	} else {
		this.variants = [];
		this.maxDurability = variants - 1;
	}
}

Material.BY_ID = {};
Material.BY_NAME = {};
Material.DATUMS = [];

Material.add = function(id, name, isBlock, variants) {
	var material = new this(id, name, isBlock, variants);
	this.BY_ID[material.id] = material;
	this.BY_NAME[material.name] = material;
	// Create datum;
	var tokens = name.split("_");
	tokens.push(name);
	tokens.push(material.name);
	tokens.push("" + material.id);
	this.DATUMS.push({
		"value": material.name,
		"tokens": tokens
	});
}

Material.prototype.getIconClass = function(damage) {
	damage = damage || 0;
	if (this.maxDurability != 0) {
		damage = 0;
	}
	return "mc-icon-" + this.id + "-" + damage;
}

